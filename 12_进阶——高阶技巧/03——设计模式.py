"""
设计模式：
    设计模式是一种编程套路,可以极大的方便程序的开发。
    最常见、最经典的设计模式,就是我们所学习的面向对象了。
    除了面向对象外,在编程中也有很多既定的套路可以方便开发,我们称之为设计模式:
    单例、工厂模式建造者、责任链、状态、备忘录、解释器、访问者、观察者、中介、模板、代理模式等等
    模式设计模式非常多
"""
"""
1.单例模式
    概念：
        就是对一个类,只获取其唯一的类实例对象，持续复用它。节省内存节省创建对象的开销
        （例如：给类创建一个实例对象，然后调用类的时候直接调用实例对象）
"""
"""
2.工厂模式
    概念：
        当需要大量创建一个类的实例的时候，可以使用工厂模式。
        即,从原生的使用类的构造去创建对象的形式迁移到,基于工厂提供的方法去创建对象的形式。
        
    使用工厂类的get_person()方法去创建具体的类对象优点：
    大批量创建对象的时候有统一的入口,易于代码维护当发生修改,
    仅修改工厂类的创建方法即可符合现实世界的模式,即由工厂来制作产品(对象)
"""